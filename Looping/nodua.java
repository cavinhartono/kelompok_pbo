public class nodua {
    public static void main(String[] args) {
        int i = 1;
        
        // Loop while untuk mencetak deret bilangan ganjil dari 1 hingga 15
        while (i <= 15) {
            System.out.print(i + " ");
            i += 2; // Increment i sebesar 2 untuk mendapatkan bilangan ganjil berikutnya
        }
    }
}
